let currentLang = localStorage.getItem('ul-lang') || 'en';

function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'ar' : 'en';
  localStorage.setItem('ul-lang', currentLang);
  applyLanguage();
}

function applyLanguage() {
  const isAR = currentLang === 'ar';

  // Update toggle button
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = isAR ? 'EN' : 'عربي';

  // Set direction but keep logo LTR
  document.documentElement.setAttribute('dir', isAR ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', isAR ? 'ar' : 'en');

  // Keep logo always LTR
  document.querySelectorAll('.logo, .logo-wordmark').forEach(el => {
    el.style.direction = 'ltr';
  });

  if (isAR) {
    document.body.style.fontFamily = "'Noto Sans Arabic', 'Segoe UI', sans-serif";
  } else {
    document.body.style.fontFamily = '';
  }

  // Translate all text elements
  const selectors = [
    'h1', 'h2', 'h3', 'h4', 'h5',
    'p', 'a', 'button', 'span', 'label', 'option',
    '.section-label', '.section-subtitle',
    '.hero-stat-label', '.stat-label',
    '.about-feature', '.about-badge-text',
    '.service-link', '.capability-item',
    '.office-sub', '.nav-cta',
    '.hero-badge', '.scroll-indicator span',
  ].join(', ');

  document.querySelectorAll(selectors).forEach(el => {
    // Skip elements with children that are not just text
    // Only translate leaf text or elements with simple text content
    if (el.closest('.logo-wordmark') || el.closest('.logo')) return;
    if (el.tagName === 'SCRIPT' || el.tagName === 'STYLE') return;

    const text = el.textContent.trim();
    if (!text) return;

    if (isAR) {
      if (T[text] && !el.getAttribute('data-en')) {
        el.setAttribute('data-en', text);
        el.textContent = T[text];
      }
    } else {
      const original = el.getAttribute('data-en');
      if (original) {
        el.textContent = original;
        el.removeAttribute('data-en');
      }
    }
  });

  // Handle placeholders on inputs/textareas
  document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(el => {
    const placeholder = el.getAttribute('placeholder');
    if (isAR) {
      if (T[placeholder] && !el.getAttribute('data-en-ph')) {
        el.setAttribute('data-en-ph', placeholder);
        el.setAttribute('placeholder', T[placeholder]);
      }
    } else {
      const orig = el.getAttribute('data-en-ph');
      if (orig) {
        el.setAttribute('placeholder', orig);
        el.removeAttribute('data-en-ph');
      }
    }
  });

  // Handle complex elements with mixed content (h1 with <br> and <span>)
  document.querySelectorAll('h1, h2').forEach(el => {
    if (el.closest('.logo-wordmark')) return;
    // Translate gradient-text spans
    el.querySelectorAll('.gradient-text').forEach(span => {
      const text = span.textContent.trim();
      if (isAR && T[text] && !span.getAttribute('data-en')) {
        span.setAttribute('data-en', text);
        span.textContent = T[text];
      } else if (!isAR && span.getAttribute('data-en')) {
        span.textContent = span.getAttribute('data-en');
        span.removeAttribute('data-en');
      }
    });
    // Translate bare text nodes (e.g. "Connecting the", "to the World")
    el.childNodes.forEach(node => {
      if (node.nodeType !== 3) return; // text nodes only
      const text = node.textContent.trim();
      if (!text) return;
      if (isAR && T[text] && !node._origText) {
        node._origText = node.textContent;
        node.textContent = node.textContent.replace(text, T[text]);
      } else if (!isAR && node._origText) {
        node.textContent = node._origText;
        delete node._origText;
      }
    });
  });
}

// Apply on load
if (currentLang === 'ar') {
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyLanguage);
  } else {
    applyLanguage();
  }
}
